    private void tablaSolicitudesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaSolicitudesMouseClicked
        int filaSeleccionada= tablaSolicitudes.getSelectedRow();
        ModeloTablaSolicitudes mts = (ModeloTablaSolicitudes) tablaSolicitudes.getModel();
        
        solicitudSeleccionada = mts.obtenerSolicitud(filaSeleccionada);
        
        aceptarButton.setEnabled(true);
        denegarButton.setEnabled(true);
    }//GEN-LAST:event_tablaSolicitudesMouseClicked

    private void aceptarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarButtonActionPerformed
        ModeloTablaSolicitudes mts = (ModeloTablaSolicitudes) tablaSolicitudes.getModel();
        
        solicitudSeleccionada = mts.obtenerSolicitud(tablaSolicitudes.getSelectedRow());
        if (solicitudSeleccionada != null) {
            fa.aumentarStockPorSolicitud(solicitudSeleccionada);
            fa.eliminarSolicitud(solicitudSeleccionada);
        }
        buscarSolicitudes();
    }//GEN-LAST:event_aceptarButtonActionPerformed

    private void denegarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_denegarButtonActionPerformed
        ModeloTablaSolicitudes mts = (ModeloTablaSolicitudes) tablaSolicitudes.getModel();
        
        solicitudSeleccionada = mts.obtenerSolicitud(tablaSolicitudes.getSelectedRow());
        if (solicitudSeleccionada != null)
            fa.eliminarSolicitud(solicitudSeleccionada);
        buscarSolicitudes();
    }//GEN-LAST:event_denegarButtonActionPerformed

