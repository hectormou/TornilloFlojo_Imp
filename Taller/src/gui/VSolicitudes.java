    public void buscarSolicitudes(){
        ModeloTablaSolicitudes m;

        m=(ModeloTablaSolicitudes) tablaSolicitudes.getModel();
        m.setFilas(fa.obtenerSolicitudes());
        if (m.getRowCount() > 0) {
            tablaSolicitudes.setRowSelectionInterval(0, 0);
            tablaSolicitudes.requestFocus();
            denegarButton.setEnabled(true);
            aceptarButton.setEnabled(true);
           // Vehiculo vehiculo= m.obtenerVehiculo(0);
           // setBotonesVehiculo(vehiculo);
        } else  {
            denegarButton.setEnabled(false);
            aceptarButton.setEnabled(false);
        }
    }




