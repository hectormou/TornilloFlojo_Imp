    private boolean esNumeroValido(String texto) {
        if (texto.isBlank() || texto == null) return false;
        else return texto.matches("[1-9]\\d*");
    }

    private void inicializarComponentes() {
        errorLabel.setVisible(false);
        nombreTextField.setText(repuestoSeleccionado.getNombre());
        nombreTextField.setEditable(false);
        idTextField.setText(repuestoSeleccionado.getId().toString());
        idTextField.setEditable(false);
        cantidadActualTextField.setText(repuestoSeleccionado.getStock().toString());
        cantidadActualTextField.setEditable(false);
    }
    
