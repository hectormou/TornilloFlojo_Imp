    public void buscarUsuarios(String id, String nombre){
        ModeloTablaUsuarios m;

        m=(ModeloTablaUsuarios) tablaUsuarios.getModel();
        m.setFilas(fa.obtenerUsuarios(id, nombre));
        if (m.getRowCount() > 0) {
            tablaUsuarios.setRowSelectionInterval(0, 0);
            obtenerUsuarioSeleccionado();
            btnBorrar.setEnabled(true);
        }
        else{
            btnBorrar.setEnabled(false);
            ModificaClave.setText("");
            ModificaId.setText("");
            ModificaNombre.setText("");
            ModificaEmail.setText("");
            ModificaDireccion.setText("");
            ModificaTipo.setSelectedItem("Normal");
        }
    }
    
    public void obtenerUsuarioSeleccionado(){
        if(tablaUsuarios.getSelectedRow()!=-1){
            ModeloTablaUsuarios m;
            m=(ModeloTablaUsuarios) tablaUsuarios.getModel();
            filaSeleccionada(m.obtenerUsuario(tablaUsuarios.getSelectedRow()));
        }
    }
    
    public void filaSeleccionada(aplicacion.Mecanico u){
        ModificaNombre.setText(u.getNombre());
        ModificaClave.setText(u.getClave());
        ModificaEmail.setText(u.getEmail());
        ModificaDireccion.setText(u.getDireccion());
        ModificaId.setText(u.getIdUsuario());
        ModificaTipo.setSelectedItem(u.getTipoUsuario());
    }
    
    public boolean existeUsuario(String id){
        return fa.existeUsuario(id);
    }
    
    public void editarUsuario(String id, String clave, String nombre, String direccion, String email, String tipo){
        fa.editarUsuario(id, clave, nombre, direccion, email, tipo);
    }
    
    public void crearUsuario(String id, String clave, String nombre, String direccion, String email, String tipo){
        fa.crearUsuario(id, clave, nombre, direccion, email, tipo);
    }
    
    public void borrarUsuario(String id){
        fa.borrarUsuario(id);
    }
