    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if(ModificaId.getText().isBlank() || ModificaNombre.getText().isBlank()){
            VAviso va = new VAviso(this.padre, true, "El campo id o nombre no pueden estar vacíos");
            va.setVisible(true);
        }
        else if(existeUsuario(ModificaId.getText())){
            editarUsuario(ModificaId.getText(), ModificaClave.getText(), ModificaNombre.getText(), ModificaDireccion.getText(), ModificaEmail.getText(), ModificaTipo.getSelectedItem().toString());
        } else{
            crearUsuario(ModificaId.getText(), ModificaClave.getText(), ModificaNombre.getText(), ModificaDireccion.getText(), ModificaEmail.getText(), ModificaTipo.getSelectedItem().toString());
        }
        buscarUsuarios("","");
    }//GEN-LAST:event_btnGuardarActionPerformed

